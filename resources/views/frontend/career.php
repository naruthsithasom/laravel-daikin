    $( '.item_positionlist > .topicposition' ).click(function (event) {
	  if (  $(this).siblings('.content_position').is( ":hidden" ) ) {
            $('.item_positionlist').removeClass("active");
            $('.content_position').slideUp();
            $(this).parent('.item_positionlist').addClass("active");
            $(this).siblings('.content_position').slideDown();
	  } else {
          $('.item_positionlist').removeClass("active");
            $('.content_position').slideUp();
	  }
	  event.stopPropagation();
	});
</script>
